CREATE TABLE produto (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   nome VARCHAR(255) NOT NULL,
   unidade_medida VARCHAR(25) NOT NULL,
   preco DOUBLE PRECISION NOT NULL,
   categoria_id BIGINT,
   CONSTRAINT pk_produto PRIMARY KEY (id)
);

